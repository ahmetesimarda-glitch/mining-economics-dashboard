FIELD_ORDER = [
    "manufacturer",
    "model",
    "category",
    "description",
    "capacityLabel",
    "payloadTons",
    "bucketCapacityM3",
    "enginePowerKw",
    "operatingWeightTons",
    "fuelTankCapacityL",
    "purchasePriceUsd",
    "usefulLifeYears",
    "availabilityPct",
    "powerType",
    "notes",
    "searchAliasesExtra",
]


Spec = dict[str, Any]


def spec(
    manufacturer: str,
    model: str,
    category: str,
    description: str,
    capacity_label: str,
    price: int,
    **kwargs: Any,
) -> Spec:
    row: Spec = {
        "manufacturer": manufacturer,
        "model": model,
        "category": category,
        "description": description,
        "capacityLabel": capacity_label,
        "purchasePriceUsd": price,
    }
    row.update({key: value for key, value in kwargs.items() if value is not None})
    return row


def by_payload(
    rows: Iterable[tuple[str, str, str, float | None, int, int | None]],
    category: str,
    description: str,
) -> list[Spec]:
    return [
        spec(
            manufacturer,
            model,
            category,
            description,
            f"{payload:g} t payload class" if payload is not None else label,
            price,
            payloadTons=payload,
            enginePowerKw=power,
            usefulLifeYears=12 if payload and payload >= 140 else None,
        )
        for manufacturer, model, label, payload, price, power in rows
    ]


def by_bucket(
    rows: Iterable[tuple[str, str, float | None, int, int | None, float | None]],
    category: str,
    description: str,
) -> list[Spec]:
    return [
        spec(
            manufacturer,
            model,
            category,
            description,
            f"{bucket:g} m3 bucket class" if bucket is not None else label,
            price,
            bucketCapacityM3=bucket,
            enginePowerKw=power,
            operatingWeightTons=weight,
            usefulLifeYears=12 if price >= 3_500_000 else None,
        )
        for manufacturer, model, bucket, price, power, weight in rows
        for label in ["Configuration-dependent bucket class"]
    ]


def simple_models(
    manufacturer: str,
    models: Iterable[str],
    category: str,
    description: str,
    capacity_label: str,
    price: int,
    step: int = 50_000,
    **kwargs: Any,
) -> list[Spec]:
    return [
        spec(
            manufacturer,
            model,
            category,
            description,
            capacity_label,
            price + index * step,
            **kwargs,
        )
        for index, model in enumerate(models)
    ]


def render_value(value: Any) -> str:
    if isinstance(value, str):
        return json.dumps(value)
    if isinstance(value, bool):
        return "true" if value else "false"
    return str(value)


def render_ts_array(export_name: str, rows: list[Spec]) -> str:
    blocks = []
    for row in rows:
        lines = []
        for key in FIELD_ORDER:
            if key not in row:
                continue
            lines.append(f"    {key}: {render_value(row[key])},")
        blocks.append("  {\n" + "\n".join(lines) + "\n  }")
    return (
        "import type { CompactEquipmentSpec } from '../helpers';\n\n"
        "/** Auto-generated by scripts/generate-equipment-catalog.py. */\n"
        f"export const {export_name}: CompactEquipmentSpec[] = [\n"
        + ",\n".join(blocks)
        + "\n];\n"
    )


def write_dataset(filename: str, export_name: str, rows: list[Spec]) -> None:
    (OUT_DIR / filename).write_text(render_ts_array(export_name, rows), encoding="utf-8")


def dedupe(rows: list[Spec]) -> list[Spec]:
    seen: set[tuple[str, str]] = set()
    unique: list[Spec] = []
    duplicates: list[str] = []
    for row in rows:
        key = (row["manufacturer"].casefold(), row["model"].casefold())
        if key in seen:
            duplicates.append(f"{row['manufacturer']} {row['model']}")
            continue
        seen.add(key)
        unique.append(row)
    if duplicates:
        print("Deduplicated duplicate manufacturer+model combinations:")
        for duplicate in duplicates:
            print(f"  - {duplicate}")
    return unique


