def write_index(exports: list[tuple[str, str]]) -> None:
    imports = "\n".join(
        f"import {{ {export_name} }} from './{filename.removesuffix('.ts')}';"
        for filename, export_name in exports
    )
    export_block = "\n".join(
        f"export {{ {export_name} }} from './{filename.removesuffix('.ts')}';"
        for filename, export_name in exports
    )
    spreads = ",\n  ".join(f"...{export_name}" for _, export_name in exports)
    content = (
        "import type { CompactEquipmentSpec } from '../helpers';\n"
        f"{imports}\n\n"
        f"{export_block}\n\n"
        "export const ALL_COMPACT_EQUIPMENT: CompactEquipmentSpec[] = [\n"
        f"  {spreads},\n"
        "];\n"
    )
    (OUT_DIR / "index.ts").write_text(content, encoding="utf-8")


def write_seed_entry() -> None:
    content = """import { buildSeedRows, type EquipmentCatalogSeedRow } from './seed/helpers';
import { ALL_COMPACT_EQUIPMENT } from './seed/datasets';

export type { EquipmentCatalogSeedRow } from './seed/helpers';

export function buildEquipmentCatalogSeedRows(): EquipmentCatalogSeedRow[] {
  return buildSeedRows(ALL_COMPACT_EQUIPMENT);
}
"""
    (ROOT / "lib" / "master-data" / "equipment-seed-data.ts").write_text(
        content, encoding="utf-8"
    )


def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    all_rows = [row for _, _, rows in DATASETS for row in rows]
    unique_rows = dedupe(all_rows)
    if len(unique_rows) != len(all_rows):
        unique_by_key = {
            (row["manufacturer"].casefold(), row["model"].casefold()): row
            for row in unique_rows
        }
        deduped_datasets: list[tuple[str, str, list[Spec]]] = []
        for filename, export_name, rows in DATASETS:
            filtered = []
            for row in rows:
                key = (row["manufacturer"].casefold(), row["model"].casefold())
                if unique_by_key.get(key) is row:
                    filtered.append(row)
            deduped_datasets.append((filename, export_name, filtered))
    else:
        deduped_datasets = DATASETS

    for filename, export_name, rows in deduped_datasets:
        write_dataset(filename, export_name, rows)
    write_index([(filename, export_name) for filename, export_name, _ in deduped_datasets])
    write_seed_entry()

    final_rows = [row for _, _, rows in deduped_datasets for row in rows]
    category_counts = Counter(row["category"] for row in final_rows)
    total = len(final_rows)

    print("Category counts:")
    for category in sorted(category_counts):
        print(f"  {category}: {category_counts[category]}")
    print(f"Total: {total}")
    print("Files written:")
    for filename, _, _ in deduped_datasets:
        print(f"  {OUT_DIR / filename}")
    print(f"  {OUT_DIR / 'index.ts'}")
    print(f"  {ROOT / 'lib/master-data/equipment-seed-data.ts'}")

    if not 400 <= total <= 450:
        raise SystemExit(f"Generated total {total} is outside requested 400-450 range")


if __name__ == "__main__":
    main()
